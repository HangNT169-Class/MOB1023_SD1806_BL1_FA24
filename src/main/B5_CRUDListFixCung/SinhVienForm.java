    // Hien thi len table
    private void showDataTable(List<SinhVien> listSinhVien) {
        // Xoa toan bo du lieu dang co tren table
        dtm.setRowCount(0);
        for (SinhVien sv : listSinhVien) {
            dtm.addRow(new Object[]{
                sv.getMssv(), sv.getTen(), sv.getTuoi(), sv.getGioiTinh(), sv.getDiaChi()
            });
        }
    }

