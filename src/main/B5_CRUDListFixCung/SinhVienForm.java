    private void detailSinhVien(int index) {
        SinhVien sv = service.getAll().get(index);
        txtDiaChi.setText(sv.getDiaChi());
        txtMaSV.setText(sv.getMssv());
        txtTen.setText(sv.getTen());
        // ep kieu: int -> String
        // C1: tuong minh
        // txtTuoi.setText(String.valueOf(sv.getTuoi()));
        // C2: +"": TAT CA CAC LOAI KIEU DU LIEU CHI CAN CONG "" => DEFAULT TRO THANH STRING
        txtTuoi.setText(sv.getTuoi() + "");
        // radio :  C1: if else 
        // C2: toan tu 3 ngoi
        rdNam.setSelected(sv.getGioiTinh());
        rdNu.setSelected(!sv.getGioiTinh());
        // VD: sv.getGioiTinh = true 
    }

